using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class CombatStats : MonoBehaviour
{
    //The crit chance for this character's attacks. Default is 5%
    [Range(0, 100)]
    public int critChance = 5;

    //The accuracy for this character's attacks. Default is 95%
    [Range(0, 100)]
    public int accuracy = 95;

    //The chance that this character completely evades damage. Default is 0%
    [Range(0, 100)]
    public int evasion = 0;

    [Space(8)]

    //How many spaces this character can move in combat
    public int speed = 5;

    [Space(8)]

    //How long it takes for this character's turn to come up in combat
    public int initiative = 10;

    //The list of active effects on this character during combat
    private List<Effect> activeEffects;
}
